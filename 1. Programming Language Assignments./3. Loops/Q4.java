// 4. Write a program to print the odd and even numbers.

public class Q4 {

    public static void main(String[] args) {

        // print the odd and even numbers from 1 to 10

        for(int i = 0; i < 11; i++){
            if(i % 2 == 0) System.out.println(i + " Even");
            else System.out.println(i + " Odd");
        }

    }
}
